# What is the difference between > and >> ?
# Write your answer here:

>  - Add to the new/old file what you want to write,
   but it will delete what was written before!!
 , example: echo "Hello Elad" > 1.txt

>> - Add to the new file what you want to write.....
In addition it can add to an old file, but a new line with what you want to write.
Example: If I have a file that already has the sentence: Hello, my name is Elad
, and I want to add a new line to the file so I use with >>.
But if I want to add a sentence/code to a new file
it doesn't matter > or >> but it's better to use >>
Because the file is not deleted if you get confused and write something in the file
